    private void btnTruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTruActionPerformed
        // TODO add your handling code here:
        if (KiemTranull()){
        double a = Double.parseDouble(txtFirst.getText());
        double b = Double.parseDouble(txtSecond.getText());
        double tru = a-b;
        txtResult.setText(""+tru); //txt.Result.setText(Double.toString(Tru));
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "Không được để trống");
        }
    }//GEN-LAST:event_btnTruActionPerformed
