    private void btnCongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCongActionPerformed
        // TODO add your handling code here:
        if(KiemTranull()) {
        double frist = Double.parseDouble(txtFirst.getText());
        double second = Double.parseDouble(txtSecond.getText());
        double  cong=0;
        cong= frist+second;
        txtResult.setText(""+cong);
        JOptionPane.showMessageDialog(rootPane, "ket qua la:"+cong);// hien thi ra man hinh
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "không được để trống");
        }
    }//GEN-LAST:event_btnCongActionPerformed
